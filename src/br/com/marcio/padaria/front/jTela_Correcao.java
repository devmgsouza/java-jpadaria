    private void consultaRetiradas(){
        RetiradasImp rimp = new RetiradasImp();
			SimpleDateFormat data = new SimpleDateFormat("dd/MM/yyyy"); 
			ArrayList<Retiradas> lista;
			Retiradas retirada;
			try {
				lista = rimp.pesquisar("data", data.format(new Date()));
				jTableCorrecao.setAutoCreateRowSorter(true);
                                 jTableCorrecao.setModel(new TableModelRetiradas((ArrayList<Retiradas>) lista));
			} catch (RetiradasException e) {
				JOptionPane.showMessageDialog(null, e.getMessage(), 
                "ERRO", JOptionPane.ERROR_MESSAGE);
			}
    }
    
    private void consultaDespesas(){
        DespesasImp dimp = new DespesasImp();
            SimpleDateFormat data = new SimpleDateFormat("dd/MM/yyyy");
            ArrayList<Despesas> lista;
            try {
                lista = dimp.pesquisar("data", data.format(new Date()));
                jTableCorrecao.setAutoCreateRowSorter(true);
                jTableCorrecao.setModel(new TableModelDespesas((ArrayList<Despesas>) lista));
            } catch (DespesasException e){
                JOptionPane.showMessageDialog(null, e.getMessage(), 
                "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        
    }
    
    private void consultaPDV(){
        PrincipalImp pimp = new PrincipalImp();
        SimpleDateFormat data = new SimpleDateFormat("dd/MM/yyyy");
        ArrayList<Principal> lista;
        try {
            lista = pimp.pesquisar("data", data.format(new Date()));
            jTableCorrecao.setAutoCreateRowSorter(true);
            jTableCorrecao.setModel(new TableModelPrincipal((ArrayList<Principal>)lista));
            
        } catch (PrincipalException e ) {
            JOptionPane.showMessageDialog(null, e.getMessage(), 
                "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
