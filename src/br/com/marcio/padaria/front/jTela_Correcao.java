    private void jTableCorrecaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableCorrecaoMouseClicked
        if (evt.getClickCount() == 2 && !evt.isConsumed()) {   
             int lin = jTableCorrecao.getSelectedRow();
             //System.out.println(jTableCorrecao.getValueAt(lin, 0));
             int pk_correcao = (int)jTableCorrecao.getValueAt(lin, 0);
            
             if (jRadioButtonDespesas.isSelected()) {
               
                Despesas despesa = new Despesas();
                despesa.setCd_despesa(pk_correcao);
                //despesa.setData("");
                despesa.setDescricao((String)jTableCorrecao.getValueAt(lin, 3));
               
                jInputBox telaInput = new jInputBox("Digite o novo valor para despesa:","despesa", despesa);
                telaInput.setModal(true);
                telaInput.setVisible(true);
                consultaDespesas();
                
             
            }  else if (jRadioButtonRetiradas.isSelected()){
             
                Retiradas retirada = new Retiradas();
                retirada.setCd_retiradas(pk_correcao);
                jInputBox telaInput = new jInputBox("Digite o novo valor para retirada:","retirada", retirada);
                telaInput.setModal(true);
                telaInput.setVisible(true);
                consultaRetiradas();
                             

            } else if (jRadioButtonPDV.isSelected()){
                 Principal principal = new Principal();
                 principal.setCd_principal(pk_correcao);
                 jInputBox telaInput = new jInputBox("Digite o novo valor para entrada:","PDV", principal);
                 telaInput.setModal(true);
                 telaInput.setVisible(true);
                 consultaPDV();
            }
            
            
        }
    }//GEN-LAST:event_jTableCorrecaoMouseClicked

