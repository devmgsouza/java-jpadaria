    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Double totalRetirado = Double.parseDouble(jTextFieldTotalRetirado.getText());
        Double saldoTotalCaixa = Double.parseDouble(jTextFieldSaldoCaixa.getText());
        Double valorRetirado = Double.parseDouble((jTextFieldValorRetirada.getText()).replace(",", "."));
       
        
        if (!(valorRetirado > (saldoTotalCaixa - totalRetirado))){
           Retiradas retirada = new Retiradas();
           RetiradasImp rimp = new RetiradasImp();
           retirada.setData(dataHoje);
           retirada.setValor(valorRetirado.toString());
            try {
                rimp.validaRetirada(retirada);
                rimp.inserir(retirada);
                JOptionPane.showMessageDialog(null, "Valor retirado!",
                       "Informativo", JOptionPane.INFORMATION_MESSAGE);
                jTextFieldValorRetirada.setText("");
                somaRetiradas();
             
             } catch (RetiradasException ex) {
               JOptionPane.showMessageDialog(null, "Não foi possível efetuar esta retirada, contate o Administrador do sistema!",
                       "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Saldo indisponível para esta retirada!","Atenção", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

