    ArrayList<?> sumList;
    private void saldoCaixa(){
            Principal principal;
            PrincipalImp pimp = new PrincipalImp();
            
           jTextFieldSaldoCaixa.setText("0.00");
        try {
            sumList = pimp.somarValor(dataHoje, dataHoje);
            principal = (Principal) sumList.remove(0);
            if (!principal.isNullValor()){
               //32 bits
               //jTextFieldSaldoCaixa.setText(String.format("%.2f", principal.getValor())); 
               
                //64 bits
                jTextFieldSaldoCaixa.setText(principal.getValor()); 
            } 
        } catch (PrincipalException ex) {
            Logger.getLogger(jTela_Retiradas.class.getName()).log(Level.SEVERE, null, ex);
        }
                   
      }
    private void somaRetiradas(){
            Retiradas retirada;
            RetiradasImp rimp = new RetiradasImp();
            
                jTextFieldTotalRetirado.setText("0.00");
        try {
            sumList = rimp.somarValor(dataHoje, dataHoje);
            retirada = (Retiradas) sumList.remove(0);
            if(!retirada.isNullValor()){
             //jTextFieldTotalRetirado.setText(String.format("%.2f", retirada.getValor()));
             jTextFieldTotalRetirado.setText(String.format(retirada.getValor())); 
            }
        } catch (RetiradasException ex) {
            Logger.getLogger(jTela_Retiradas.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }
