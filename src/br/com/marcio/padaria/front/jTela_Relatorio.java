    private void jButtonPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPrintActionPerformed
                JasperReport report = null;
           
         try {
             
            
                     switch ((String)jComboBoxFiltro.getSelectedItem()) {
                         case "Caixa" :
                                 report = JasperCompileManager
                     .compileReport(caminho_relatorio + "caixaReport.jrxml");
                         break;
                             
                         case "Despesas" :
                             report = JasperCompileManager
                     .compileReport(caminho_relatorio + "despesasReport.jrxml");
                         break;
                             
                         case "Pendências" :
                             report = JasperCompileManager
                     .compileReport(caminho_relatorio + "pendenciasReport.jrxml");
                         break;
                             
                         case "PDV" :
                             report = JasperCompileManager
                     .compileReport(caminho_relatorio + "principalReport.jrxml");
                         break;
                             
                         case "Retiradas" :
                             report = JasperCompileManager
                     .compileReport(caminho_relatorio + "retiradasReport.jrxml");
                         break;                               
                         
                     }
                     
             JasperPrint print = JasperFillManager.fillReport(report, null,
				new JRBeanCollectionDataSource(listaPrint));

		JasperViewer.viewReport(print, false); 
                
         } catch (JRException e) {
             //Logger.getLogger(jTela_Relatorio.class.getName()).log(Level.SEVERE, null, ex);
             JOptionPane.showMessageDialog(null,e.getMessage());
         }
		       
    }//GEN-LAST:event_jButtonPrintActionPerformed


