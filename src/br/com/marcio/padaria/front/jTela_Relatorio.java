    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        	String dataInicio = new 
        SimpleDateFormat("dd/MM/yyyy").format(jDateInicio.getDate());
                String dataFim = new 
        SimpleDateFormat("dd/MM/yyyy").format(jDateFim.getDate());       
                             
                
                switch ((String)jComboBoxFiltro.getSelectedItem()){
                    case "Caixa" :
                        CaixaImp cimp = new CaixaImp();
                        Caixa caixa;
                    try {
                         lista = cimp.pesquisar("data",dataInicio, dataFim); 
                         listaPrint = lista;
                         
                         jTableResultado.setAutoCreateRowSorter(true);
                        jTableResultado.setModel(new TableModelCaixa((ArrayList<Caixa>) lista));
                        lista = cimp.somarValor(dataInicio, dataFim);
                        caixa = (Caixa) lista.remove(0);
                        jLabelSomaTotal.setText(caixa.getValor());
                    } catch (CaixaException e) {	
                    }
                     break;
                        
                        case "PDV" :
                        PrincipalImp pimp = new PrincipalImp();
                        Principal principal;
                    try {
                        lista = pimp.pesquisar("data",dataInicio, dataFim);
                        listaPrint = lista;
                        
                        jTableResultado.setAutoCreateRowSorter(true);
                        jTableResultado.setModel(new TableModelPrincipal((ArrayList<Principal>) lista));
                        
                        lista = pimp.somarValor(dataInicio, dataFim);
                        principal = (Principal) lista.remove(0);
                        jLabelSomaTotal.setText(principal.getValor());
                        
                        
                    } catch (PrincipalException e) {	
                    }
                     break;
                            
                            case "Pendências" :
                        ClientePendenciaImp cdimp = new ClientePendenciaImp();
                        ClientePendencia clientePendencia;
                    try {
                        lista = cdimp.pesquisar(dataInicio, dataFim);
                        listaPrint = lista;
                        jTableResultado.setAutoCreateRowSorter(true);
                        jTableResultado.setModel(new TableModelClientePendencia((ArrayList<ClientePendencia>) lista));	
                        lista = cdimp.somarValor(dataInicio, dataFim);
                        clientePendencia = (ClientePendencia) lista.remove(0);
                        jLabelSomaTotal.setText(clientePendencia.getValor());
                   	
                    } catch (ClientesException | PendenciasException e) {
                 
                }
                     break;
                                
                            case "Despesas" :
                        DespesasImp dimp = new DespesasImp();
                        Despesas despesa;
                    try {
                        lista = dimp.pesquisar("data", dataInicio, dataFim);
                        listaPrint = lista;
                        jTableResultado.setAutoCreateRowSorter(true);
                        jTableResultado.setModel(new TableModelDespesas((ArrayList<Despesas>) lista));	
                   	lista = dimp.somarValor(dataInicio, dataFim);
                        despesa = (Despesas) lista.remove(0);
                        jLabelSomaTotal.setText(despesa.getValor());
                        
                    } catch (DespesasException e) {
                 
                }
                     break;
                                
                         case "Retiradas" :
                        RetiradasImp rimp = new RetiradasImp();
                        Retiradas retirada;
                    try {
                        lista = rimp.pesquisar("data", dataInicio, dataFim);
                        listaPrint = lista;
                        jTableResultado.setAutoCreateRowSorter(true);
                        jTableResultado.setModel(new TableModelRetiradas((ArrayList<Retiradas>) lista));	
                   	lista = rimp.somarValor(dataInicio, dataFim);
                        retirada = (Retiradas) lista.remove(0);
                        jLabelSomaTotal.setText(retirada.getValor());
                    } catch (RetiradasException e) {
                 
                }
                     break;
                 
                }
               

    }//GEN-LAST:event_jButtonPesquisarActionPerformed

