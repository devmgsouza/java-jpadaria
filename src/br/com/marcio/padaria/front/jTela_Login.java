    private void buscarTodosOsUsuarios(){
        LoginImp loginImp = new LoginImp();
        Login login;
        ArrayList<Login> lista;
                
        
                 try {
                    lista = loginImp.pesquisar();
                    while (!lista.isEmpty()){
                        login = lista.remove(0);
                        jCombo_usuario.addItem(login.getUsuario());
                    }
                        
                    
                } catch (LoginException ex) {
                    
                }
    }
    
    
    
