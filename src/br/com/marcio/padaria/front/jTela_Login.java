    private void jButton_entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_entrarActionPerformed

        LoginImp loginImp = new LoginImp();
        Login login;
        ArrayList<Login> lista;
        String usuarioSelecionado = (String) jCombo_usuario.getSelectedItem();
        
        try {
            lista = loginImp.pesquisar("usuario", usuarioSelecionado);
            login = lista.remove(0);
            if (login.getSenha().equals(jText_senha.getText())){
                jText_senha.setText("");
                jTela_Principal.getInstance().show();
                
        } else {
                JOptionPane.showMessageDialog(null, "Senha incorreta!", "ERRO", JOptionPane.ERROR_MESSAGE);
                jText_senha.setText("");
            }
            
        } catch (LoginException ex) {
            
        }
        
     
    }//GEN-LAST:event_jButton_entrarActionPerformed

