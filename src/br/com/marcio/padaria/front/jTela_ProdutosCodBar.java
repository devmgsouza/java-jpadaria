    private void jTableProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProdutosMouseClicked
        if (evt.getClickCount() == 2 && !evt.isConsumed()) {
            IncluirAlterar();
            int lin = jTableProdutos.getSelectedRow();
            jTextFieldDescricao.setText((String) jTableProdutos.getValueAt(lin, 1));
            jTextFieldValor.setText((String) jTableProdutos.getValueAt(lin, 2));
            jTextFieldCodigo.setText(String.valueOf(jTableProdutos.getValueAt(lin, 0)));
            novo = 0;
            PesquisarCodBarras();
        }
    }//GEN-LAST:event_jTableProdutosMouseClicked

