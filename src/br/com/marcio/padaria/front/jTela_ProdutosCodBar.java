    private void IncluirAlterar(){
        jButtonIncluir.setEnabled(false);
        jButtonExcluir.setEnabled(true);
        
        jTableProdutos.setEnabled(false);
        
        jTextFieldCodigo.setEnabled(false);
        jTextFieldDescricao.setEnabled(true);
        jTextFieldValor.setEnabled(true);
        
        jButtonSalvar.setEnabled(true);
        jButtonCancelar.setEnabled(true);   
    }
    
    private void CancelarSalvar(){
        jTableProdutos.setEnabled(true);
        
        jButtonIncluir.setEnabled(true); 
        jButtonExcluir.setEnabled(false);
        
        jTextFieldDescricao.setEnabled(false);
        jTextFieldValor.setEnabled(false);
        jTextFieldCodigo.setEnabled(false);
        
        jButtonSalvar.setEnabled(false);
        jButtonCancelar.setEnabled(false); 
        
        jTextFieldDescricao.setText("");
        jTextFieldValor.setText("");
        jTextFieldCodigo.setText("");
        
       
        PesquisarProdutos();
    }
    
    private void PesquisarProdutos(){
        ProdutosImp pimp = new ProdutosImp();
        ArrayList<Produtos> lista;
        try {
            lista = pimp.pesquisar();
            jTableProdutos.setAutoCreateRowSorter(true);
            jTableProdutos.setModel(new TableModelProdutos((ArrayList<Produtos>) lista));
                    
        } catch (ProdutosException e) {
            JOptionPane.showMessageDialog(null,e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }
    
    private void PesquisarCodBarras(){
      CodBarrasImp cimp = new CodBarrasImp();
      ArrayList<CodBarras> lista;
      try {
          lista = cimp.pesquisar("cd_produto", jTextFieldCodigo.getText());
          jTableCodBarras.setAutoCreateRowSorter(true);
          jTableCodBarras.setModel(new TableModelCodBarras((ArrayList<CodBarras>) lista));
      } catch (CodBarrasException e){
          JOptionPane.showMessageDialog(null,e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
      }
      
    }
    
    
    private void AtivaDesativaJPanel(javax.swing.JPanel panel, boolean bool){
         for (int i = 0; i < panel.getComponentCount(); i++ ) {  
              panel.getComponent(i).setEnabled(bool);
            }  
    }
    
    private void setAcessibilidade() {
        JRootPane meuJPanel = getRootPane();
        meuJPanel.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(
                KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), "ESCAPE");
        meuJPanel.getRootPane().getActionMap().put("ESCAPE",
                new AbstractAction("ESCAPE") {

            @Override
            public void actionPerformed(ActionEvent e) {
                dispose();
            }
       });
    } 
    
