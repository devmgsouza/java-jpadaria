    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        Produtos produto = new Produtos();
        ProdutosImp pimp = new ProdutosImp();

        int row = jTableProdutos.getSelectedRow();
        if (novo == 0) {
            produto.setCd_produto((int) jTableProdutos.getValueAt(row, 0));
        }

        produto.setDescricao(jTextFieldDescricao.getText());
        produto.setValor(jTextFieldValor.getText().replace(",", "."));
        try {
            pimp.validaProduto(produto);
            if (novo == 0) {
                pimp.atualizar(produto);
            } else {
                pimp.inserir(produto);            
            }
            PesquisarProdutos();
            jTextFieldCodigo.setText(String.valueOf(jTableProdutos.getValueAt(
                jTableProdutos.getRowCount() -1, 0)));
            jTextFieldCodBarras.setText("");
            PesquisarCodBarras();
            AtivaDesativaJPanel(jPanelCodBarras, true);
            AtivaDesativaJPanel(jPanelProduto, false);
            

        } catch (ProdutosException ex) {
            JOptionPane.showMessageDialog(null,ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
       
        
        
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInserirActionPerformed
           int row = jTableProdutos.getSelectedRow();  
           CodBarrasImp cimp = new CodBarrasImp();
           CodBarras codbar = new CodBarras();
           codbar.setCod_bar(jTextFieldCodBarras.getText());
           codbar.setCd_produto(Integer.parseInt(jTextFieldCodigo.getText()));
        try {  
             cimp.validaCodBarras(codbar);
             cimp.inserir(codbar);
             PesquisarCodBarras();
             
        } catch (CodBarrasException ex) {
            JOptionPane.showMessageDialog(null,ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
          
    }//GEN-LAST:event_jButtonInserirActionPerformed

